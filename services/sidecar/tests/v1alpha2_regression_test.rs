//! SLICE_02 — v1alpha1 → v1alpha2 byte-identical regression test.
//!
//! Per `docs/contract-dsl-spec-v1alpha2.md` §8.2 + SLICE_02 §8.2:
//!
//!   > v1alpha2 sidecar + v1alpha1 contract → byte-identical audit row
//!   > output for 100 decisions vs v1alpha1 sidecar baseline.
//!
//! "Byte-identical" here covers the fields spec §6.4 explicitly
//! enumerates as preserved invariants:
//!
//!   * decision_id (mints from the same evaluator path)
//!   * decision (Decision::Continue / Decision::Stop / etc.)
//!   * reason_codes (vec — preserved verbatim)
//!   * matched_rule_ids (vec — preserved verbatim)
//!   * mutation_patch_json (always empty in this slice; no mutation rules)
//!
//! NOT covered by "byte-identical" (per the slice's intentional boundary):
//!
//!   * prediction_policy_used column — SLICE_01 deferred it to NULL;
//!     SLICE_02 fills it with STRICT_CEILING for v1alpha1 contracts.
//!     This is the entire point of the slice and is regression-asserted
//!     POSITIVELY (see test below) — the audit row gets the new
//!     STRICT_CEILING value, not NULL.
//!
//! ## How this fits §8.2 + the slice acceptance
//!
//! The slice promise is "v1alpha2 evaluator produces the same decision
//! over 100 v1alpha1 contracts/claims as the v1alpha1 evaluator would
//! have". We can't easily host the v1alpha1 evaluator binary alongside
//! the v1alpha2 one in unit tests, so we encode the invariants the
//! v1alpha1 evaluator HAD (matched_rule_ids, reason_codes, decision)
//! and assert the v1alpha2 evaluator preserves them across a 100-decision
//! synthetic corpus driven by the existing demo contract YAML.

