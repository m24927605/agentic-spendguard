/// Build a gzipped tarball containing only `contract.yaml` with the
/// given content. Matches the bundle layout the loader expects from
/// `deploy/demo/init/bundles/generate.sh`.
fn build_tgz_bundle(yaml: &str) -> Vec<u8> {
    use std::io::Write;
    let mut tar_bytes = Vec::new();
    {
        let mut builder = tar::Builder::new(&mut tar_bytes);
        let mut header = tar::Header::new_gnu();
        header.set_path("contract.yaml").unwrap();
        header.set_size(yaml.len() as u64);
        header.set_mode(0o644);
        header.set_mtime(0);
        header.set_cksum();
        builder.append(&header, yaml.as_bytes()).unwrap();
        builder.finish().unwrap();
    }
    let mut encoder = flate2::write::GzEncoder::new(Vec::new(), flate2::Compression::default());
    encoder.write_all(&tar_bytes).unwrap();
    encoder.finish().unwrap()
}

/// Build a BudgetClaim for the demo budget at the given atomic amount.
fn synthetic_claim(amount_atomic: &str) -> spendguard_sidecar::proto::common::v1::BudgetClaim {
    spendguard_sidecar::proto::common::v1::BudgetClaim {
        budget_id: "44444444-4444-4444-8444-444444444444".to_string(),
        unit: None,
        amount_atomic: amount_atomic.to_string(),
        direction: 1, // Debit
        window_instance_id: String::new(),
    }
}

#[test]
fn v1alpha1_legacy_loads_and_defaults_fill() {
    let c: Contract = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA1_LEGACY_YAML))
        .expect("parse legacy v1alpha1 bundle");
    // SLICE_02 §6.4: v1alpha1 contracts get default STRICT_CEILING +
    // BLOCK_NEXT_CALL fill.
    assert_eq!(c.prediction_policy, PredictionPolicy::StrictCeiling);
    for rule in &c.rules {
        assert_eq!(
            rule.run_projection_action,
            RunProjectionAction::BlockNextCall,
            "rule '{}' should get default BlockNextCall",
            rule.id
        );
    }
    assert_eq!(c.api_version, "contract.spendguard.io/v1alpha1");
}

#[test]
fn v1alpha1_canonical_loads_and_defaults_fill() {
    let c: Contract = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA1_CANONICAL_YAML))
        .expect("parse canonical v1alpha1 bundle");
    assert_eq!(c.prediction_policy, PredictionPolicy::StrictCeiling);
    assert_eq!(c.api_version, "spendguard.ai/v1alpha1");
}

#[test]
fn v1alpha2_loads_with_explicit_policy() {
    let c: Contract =
        parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA2_YAML)).expect("parse v1alpha2 bundle");
    assert_eq!(c.prediction_policy, PredictionPolicy::EmpiricalRunCeiling);
    // require-approval-large rule has explicit REQUIRE_APPROVAL action.
    let r = c
        .rules
        .iter()
        .find(|r| r.id == "require-approval-large")
        .expect("rule");
    assert_eq!(
        r.run_projection_action,
        RunProjectionAction::RequireApproval
    );
    // hard-cap-deny rule defaults to BLOCK_NEXT_CALL.
    let r = c
        .rules
        .iter()
        .find(|r| r.id == "hard-cap-deny")
        .expect("rule");
    assert_eq!(r.run_projection_action, RunProjectionAction::BlockNextCall);
    assert_eq!(c.api_version, "spendguard.ai/v1alpha2");
}

/// §8.2 byte-identical regression — the spec invariants
/// (decision, reason_codes, matched_rule_ids) must be identical between
/// the legacy v1alpha1 and the canonical v1alpha1 sources, and between
/// either v1alpha1 source and the v1alpha2 contract on per-call claims.
///
/// We exercise the corpus with 100 synthetic decisions spread across:
///   - 50 in CONTINUE band (amount < 400_000_000)
///   - 30 in REQUIRE_APPROVAL band (400_000_000 < amount < 1_000_000_000)
///   - 20 in STOP band (amount > 1_000_000_000)
///
/// Each decision compares (v1alpha1 legacy, v1alpha1 canonical, v1alpha2)
/// outputs and asserts the invariants.
#[test]
fn v1alpha2_evaluator_preserves_decisions_on_v1alpha1_contracts_100_corpus() {
    let c_legacy = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA1_LEGACY_YAML))
        .expect("parse legacy v1alpha1");
    let c_canonical = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA1_CANONICAL_YAML))
        .expect("parse canonical v1alpha1");
    let c_v1alpha2 =
        parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA2_YAML)).expect("parse v1alpha2");

    // 100-decision corpus per §8.2.
    let mut corpus: Vec<String> = Vec::new();
    // CONTINUE band: amounts [1, 100_000, 1_000_000, ... 50 entries below 400_000_000]
    for i in 0..50 {
        let amount = 1 + (i as i64) * 7_000_000;
        corpus.push(amount.to_string());
    }
    // REQUIRE_APPROVAL band: [500_000_000 .. 990_000_000, 30 entries]
    for i in 0..30 {
        let amount = 500_000_000 + (i as i64) * 15_000_000;
        corpus.push(amount.to_string());
    }
    // STOP band: [1_500_000_000 .. 21_500_000_000, 20 entries]
    for i in 0..20 {
        let amount: i64 = 1_500_000_000 + (i as i64) * 1_000_000_000;
        corpus.push(amount.to_string());
    }
    assert_eq!(
        corpus.len(),
        100,
        "corpus must hit §8.2 100-decision target"
    );

    for (i, amount) in corpus.iter().enumerate() {
        let claim = synthetic_claim(amount);
        let out_legacy = evaluate(&c_legacy, std::slice::from_ref(&claim));
        let out_canonical = evaluate(&c_canonical, std::slice::from_ref(&claim));
        let out_v1alpha2 = evaluate(&c_v1alpha2, std::slice::from_ref(&claim));

        // §8.2 invariant 1: decision matches across all 3.
        assert_eq!(
            out_legacy.decision, out_canonical.decision,
            "decision differs at corpus[{i}] (amount={amount}) legacy vs canonical"
        );
        assert_eq!(
            out_legacy.decision, out_v1alpha2.decision,
            "decision differs at corpus[{i}] (amount={amount}) legacy vs v1alpha2"
        );

        // §8.2 invariant 2: reason_codes (vec — order-sensitive).
        assert_eq!(
            out_legacy.reason_codes, out_canonical.reason_codes,
            "reason_codes differs at corpus[{i}] legacy vs canonical"
        );
        assert_eq!(
            out_legacy.reason_codes, out_v1alpha2.reason_codes,
            "reason_codes differs at corpus[{i}] legacy vs v1alpha2"
        );

        // §8.2 invariant 3: matched_rule_ids.
        // Note: matched_rule_ids includes the contract UUID as a prefix
        // (per evaluate.rs:rule_outcome) which is the SAME for all 3
        // because all 3 contracts have the same metadata.id.
        assert_eq!(
            out_legacy.matched_rule_ids, out_canonical.matched_rule_ids,
            "matched_rule_ids differs at corpus[{i}] legacy vs canonical"
        );
        assert_eq!(
            out_legacy.matched_rule_ids, out_v1alpha2.matched_rule_ids,
            "matched_rule_ids differs at corpus[{i}] legacy vs v1alpha2"
        );
    }
}

/// Spec §8.2 acceptance criteria explicit checkpoints — confirm that
/// the CONTINUE / REQUIRE_APPROVAL / STOP bands each hit the expected
/// final decision (not just that all 3 evaluators agree — that's the
/// previous test). This guards against the case where all 3 are
/// silently broken in the same way (eg the lattice merge changes
/// precedence).
#[test]
fn v1alpha2_decisions_match_demo_bundle_expectations() {
    let c = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA2_YAML)).expect("parse v1alpha2");

    use spendguard_sidecar::proto::sidecar_adapter::v1::decision_response::Decision;

    // CONTINUE band: amount under both thresholds.
    let out = evaluate(&c, &[synthetic_claim("100000000")]); // $100
    assert!(matches!(out.decision, Decision::Continue));
    assert!(out.matched_rule_ids.is_empty());

    // REQUIRE_APPROVAL band: amount > 400M but < 1B.
    let out = evaluate(&c, &[synthetic_claim("500000000")]); // $500
    assert!(matches!(out.decision, Decision::RequireApproval));
    assert_eq!(out.reason_codes, vec!["LARGE_CLAIM_REQUIRES_APPROVAL"]);

    // STOP band: amount > 1B (hard cap).
    let out = evaluate(&c, &[synthetic_claim("2000000000")]); // $2000
    assert!(matches!(out.decision, Decision::Stop));
    // Both rules fire (>400M and >1B); lattice merge picks STOP.
    assert!(out.reason_codes.contains(&"BUDGET_EXHAUSTED".to_string()));
    assert!(out
        .reason_codes
        .contains(&"LARGE_CLAIM_REQUIRES_APPROVAL".to_string()));
}

/// SLICE_02 §8.6 (audit invariant) cross-check: the contract's
/// prediction_policy value is correctly propagated to anything that
/// reads it for audit emission. The evaluator does not access
/// prediction_policy directly (per spec §3.4 design — policy
/// projects via run_projection_action, not by reading the policy
/// field at eval time); but downstream `build_audit_decision_cloudevent`
/// reads `bundle.parsed.prediction_policy.as_str()` to populate the
/// CloudEvent tag 305. This test confirms the parsed value matches
/// the canonical string form.
#[test]
fn parsed_prediction_policy_string_form_matches_audit_wire() {
    let c = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA2_YAML)).expect("parse v1alpha2");
    assert_eq!(c.prediction_policy.as_str(), "EMPIRICAL_RUN_CEILING");

    let c = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA1_LEGACY_YAML))
        .expect("parse legacy v1alpha1");
    assert_eq!(c.prediction_policy.as_str(), "STRICT_CEILING");

    let c = parse_from_tgz(&build_tgz_bundle(fixtures::V1ALPHA1_CANONICAL_YAML))
        .expect("parse canonical v1alpha1");
    assert_eq!(c.prediction_policy.as_str(), "STRICT_CEILING");
}
