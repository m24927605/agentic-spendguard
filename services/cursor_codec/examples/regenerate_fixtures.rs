fn chunk(
    model: &str,
    delta: &str,
    finish_reason: Option<&str>,
    cumulative_output_tokens: Option<u32>,
) -> CursorChatResponseChunk {
    CursorChatResponseChunk {
        model: model.to_string(),
        delta: delta.to_string(),
        finish_reason: finish_reason.map(|s| s.to_string()),
        cumulative_output_tokens,
    }
}
