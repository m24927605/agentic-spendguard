// (1) Multi-turn conversation
// ============================================================================
//
// Three-turn Cursor Agent session: system + user + assistant + user
// followup. Request frame carries the full history; response stream
// emits a 3-chunk reply ending with finish_reason="stop".

fn multiturn_fixture() -> Vec<FixtureFrame> {
    let req = CursorChatRequest {
        messages: vec![
            CursorMessage {
                role: "system".to_string(),
                content: "You are Cursor Agent. Be terse.".to_string(),
            },
            CursorMessage {
                role: "user".to_string(),
                content: "explain reservation".to_string(),
            },
            CursorMessage {
                role: "assistant".to_string(),
                content: "A pre-call hold on a budget.".to_string(),
            },
            CursorMessage {
                role: "user".to_string(),
                content: "what about release?".to_string(),
            },
        ],
        model: "claude-3.5-sonnet".to_string(),
        system: Some("You are Cursor Agent. Be terse.".to_string()),
        max_tokens: Some(256),
        temperature: Some(0.2),
    };

    let chunk1 = chunk("claude-3.5-sonnet", "Release ", None, None);
    let chunk2 = chunk("", "refunds the ", None, None);
    let chunk3 = chunk("", "hold.", Some("stop"), Some(11));
    let trailers = Bytes::from_static(b"grpc-status:0");

    vec![
        request_frame(1_716_000_000_000, &req),
        response_chunk_frame(1_716_000_000_020, &chunk1, 0x00),
        response_chunk_frame(1_716_000_000_040, &chunk2, 0x00),
        response_chunk_frame(1_716_000_000_060, &chunk3, 0x00),
        trailers_frame(1_716_000_000_080, trailers),
    ]
}

