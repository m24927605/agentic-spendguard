//! Regenerate the synthetic `.cursor-rpc` fixture corpus.
//!
//! D17 SLICE 8 ([`design.md`](../docs/specs/coverage/D17_cursor_mitm/design.md)
//! §7): the codec's fixture corpus is reproducible from a Rust
//! generator so reviewers can re-derive every byte of every fixture
//! without trusting opaque blobs.
//!
//! Usage:
//!
//! ```sh
//! cargo run --manifest-path services/cursor_codec/Cargo.toml \
//!     --example regenerate_fixtures
//! ```
//!
//! Each fixture is written under `services/cursor_codec/fixtures/synthetic/`
//! plus a sidecar `.manifest.json` with capture date, version range,
//! and field-by-field hex evidence per `review-standards.md` §2 (R2,
//! R4). Existing SLICE 1 fixtures (`synthetic_unary_v1.cursor-rpc`,
//! `synthetic_streaming_chunked_v1.cursor-rpc`) are intentionally
//! NOT touched by this binary — they were generated by the SLICE 2
//! framing tests and are committed verbatim. This binary owns the
//! NEW SLICE 8 fixtures only.

use std::path::PathBuf;

use bytes::Bytes;
use spendguard_cursor_codec::cursor_proto::{
    CursorChatRequest, CursorChatResponseChunk, Message as CursorMessage,
};
use spendguard_cursor_codec::{
    request_frame, response_chunk_frame, trailers_frame, write_fixture_bytes, FixtureFrame,
};

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let out_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("fixtures/synthetic");
    std::fs::create_dir_all(&out_dir)?;

    let mut total_bytes = 0usize;

    for (name, frames, manifest) in fixtures() {
        let bytes = write_fixture_bytes(&frames);
        if bytes.len() > 64 * 1024 {
            return Err(format!(
                "fixture {name} is {} bytes (cap is 64 KiB per review-standards.md C4)",
                bytes.len()
            )
            .into());
        }
        let fixture_path = out_dir.join(format!("{name}.cursor-rpc"));
        let manifest_path = out_dir.join(format!("{name}.cursor-rpc.manifest.json"));
        std::fs::write(&fixture_path, &bytes)?;
        std::fs::write(&manifest_path, manifest)?;
        total_bytes += bytes.len();
        println!(
            "[regenerate_fixtures] wrote {name} ({} bytes, {} frames)",
            bytes.len(),
            frames.len()
        );
    }

    println!("[regenerate_fixtures] DONE — total {total_bytes} bytes across new fixtures");
    Ok(())
}

fn fixtures() -> Vec<(&'static str, Vec<FixtureFrame>, &'static str)> {
    vec![
        (
            "synthetic_multiturn_v1",
            multiturn_fixture(),
            MULTITURN_MANIFEST,
        ),
        (
            "synthetic_tool_calls_v1",
            tool_calls_fixture(),
            TOOL_CALLS_MANIFEST,
        ),
        (
            "synthetic_error_response_v1",
            error_response_fixture(),
            ERROR_MANIFEST,
        ),
        (
            "synthetic_long_stream_v1",
            long_stream_fixture(),
            LONG_STREAM_MANIFEST,
        ),
    ]
}

