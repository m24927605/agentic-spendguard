// ============================================================================
// (4) Long stream (≥10 chunks)
// ============================================================================
//
// A 12-chunk streaming response. Mirrors a long-form Cursor Agent
// reply. Each chunk carries cumulative_output_tokens so the replay
// harness can track per-chunk token deltas.

fn long_stream_fixture() -> Vec<FixtureFrame> {
    let req = CursorChatRequest {
        messages: vec![CursorMessage {
            role: "user".to_string(),
            content: "stream a long answer".to_string(),
        }],
        model: "claude-3.5-sonnet".to_string(),
        system: None,
        max_tokens: Some(1024),
        temperature: Some(0.3),
    };

    let mut frames = vec![request_frame(1_716_000_003_000, &req)];

    // Twelve content chunks then a terminal finish chunk + trailers.
    let deltas = [
        "Once ",
        "upon ",
        "a ",
        "time ",
        "there ",
        "was ",
        "a ",
        "budget ",
        "that ",
        "always ",
        "balanced ",
        "perfectly.",
    ];
    let mut cumulative = 0u32;
    for (i, delta) in deltas.iter().enumerate() {
        // Tokens-per-chunk is delta-length / 4 floored to 1 — same
        // chars/4 heuristic the rest of the codec falls back to.
        let tokens = ((delta.len() as u32) / 4).max(1);
        cumulative += tokens;
        let model = if i == 0 { "claude-3.5-sonnet" } else { "" };
        let c = chunk(model, delta, None, Some(cumulative));
        frames.push(response_chunk_frame(
            1_716_000_003_000 + 10 + (i as u64) * 5,
            &c,
            0x00,
        ));
    }
    // Terminal data chunk: empty delta, finish_reason=stop.
    let terminal = chunk("", "", Some("stop"), Some(cumulative));
    frames.push(response_chunk_frame(
        1_716_000_003_000 + 10 + (deltas.len() as u64) * 5,
        &terminal,
        0x00,
    ));
    frames.push(trailers_frame(
        1_716_000_003_000 + 20 + (deltas.len() as u64) * 5,
        Bytes::from_static(b"grpc-status:0"),
    ));
    frames
}

