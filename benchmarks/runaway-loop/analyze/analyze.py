"""Read each runner's self-report + the mock LLM ground-truth log,
apply a centralized published-pricing table, and write RESULTS.md.

The pricing table reflects the public list prices each library is
expected to know. By computing ground truth from a shared table, we
make every runner's self-report directly comparable — no runner's
internal table influences the "actual" $ number.

Inputs (env-overridable):
  RESULTS_DIR     /results
  MOCK_LLM_LOG    /var/log/mock_llm.jsonl
  OUTPUT_PATH     /results/RESULTS.md
"""

from __future__ import annotations

import json
import os
import shutil
from collections import defaultdict
from pathlib import Path

RESULTS_DIR = Path(os.environ.get("RESULTS_DIR", "/results"))
MOCK_LLM_LOG = Path(os.environ.get("MOCK_LLM_LOG", "/var/log/mock_llm.jsonl"))
OUTPUT_PATH = Path(os.environ.get("OUTPUT_PATH", "/results/RESULTS.md"))
HOST_COPY = Path("/host/RESULTS.md")

# Published USD per 1M tokens. Sources: openai.com/api/pricing,
# anthropic.com/pricing as of 2026-05.
PRICING_USD_PER_M = {
    "gpt-4o": {"input": 2.50, "output": 10.00},
    "gpt-4o-mini": {"input": 0.15, "output": 0.60},
    "o1": {"input": 15.00, "output": 60.00},
    "o1-mini": {"input": 1.10, "output": 4.40},
    "claude-3-5-sonnet-20241022": {"input": 3.00, "output": 15.00},
}


def cost_for(model: str, input_tokens: int, output_tokens: int) -> float:
    p = PRICING_USD_PER_M.get(model)
    if not p:
        # Don't silently zero out unknown models — that would let a
        # model-name typo make a runner look free. Force the operator
        # to add the model to the pricing table or rename in the runner.
        raise KeyError(
            f"model {model!r} not in PRICING_USD_PER_M — "
            f"add it to analyze/analyze.py or rename in the runner. "
            f"Known: {sorted(PRICING_USD_PER_M)}"
        )
    return (input_tokens / 1_000_000) * p["input"] + (
        output_tokens / 1_000_000
    ) * p["output"]


REQUIRED_RUNNERS = {"agentbudget", "agentguard", "spendguard"}


def load_runner_records() -> list[dict]:
    records = []
    for path in sorted(RESULTS_DIR.glob("*.json")):
        if path.name in {"all.json"}:
            continue
        with path.open() as f:
            records.append(json.load(f))
    seen = {r["runner"] for r in records}
    missing = REQUIRED_RUNNERS - seen
    if missing:
        # A missing runner file means a runner crashed / was skipped.
        # Better to fail loudly than to publish a partial table that
        # implies the missing tool wasn't tested.
        raise RuntimeError(
            f"missing runner result file(s): {sorted(missing)} — "
            f"saw only {sorted(seen)}. Did a runner crash?"
        )
    return records


def aggregate_mock_log() -> dict[str, dict]:
    by_runner: dict[str, dict] = defaultdict(
        lambda: {
            "calls": 0,
            "input_tokens": 0,
            "output_tokens": 0,
            "cost_usd": 0.0,
            "model": None,
        }
    )
    if not MOCK_LLM_LOG.exists():
        return by_runner
    with MOCK_LLM_LOG.open() as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            evt = json.loads(line)
            r = evt["runner"]
            inp = int(evt["input_tokens"])
            out = int(evt["output_tokens"])
            by_runner[r]["calls"] += 1
            by_runner[r]["input_tokens"] += inp
            by_runner[r]["output_tokens"] += out
            by_runner[r]["cost_usd"] += cost_for(evt["model"], inp, out)
            by_runner[r]["model"] = evt["model"]
    return by_runner


def overshoot_pct(spent: float, budget: float) -> float:
    if budget == 0:
        return 0.0
    return ((spent - budget) / budget) * 100


def render_markdown(runners: list[dict], ground: dict[str, dict]) -> str:
    lines: list[str] = []
    lines.append("# Runaway-loop benchmark — RESULTS\n")
    lines.append(
        "Generated by `benchmarks/runaway-loop/analyze/analyze.py`. "
        "Ground-truth `$ spent` is computed from the mock LLM's call "
        "log times a centralized published-pricing table — every "
        "runner is scored against the same number, regardless of what "
        "its internal pricing table thinks.\n"
    )
    lines.append("## Summary\n")
    lines.append(
        "| Runner | Budget | Wire calls¹ | Runner-reported succeeded | Ground-truth $ spent | Overshoot vs budget | Aborted at call # | Exception |\n"
        "|---|---:|---:|---:|---:|---:|---:|---|"
    )
    for rec in runners:
        runner = rec["runner"]
        budget = float(rec["budget_usd"])
        truth = ground.get(
            runner, {"calls": 0, "cost_usd": 0.0, "model": "?"}
        )
        spent = truth["cost_usd"]
        ovr = overshoot_pct(spent, budget)
        ovr_str = f"+{ovr:.1f}%" if ovr >= 0 else f"{ovr:.1f}%"
        abort_at = rec.get("abort_at_call")
        exc = rec.get("abort_exception_class") or "—"
        lines.append(
            f"| `{runner}` | ${budget:.2f} | {truth['calls']} | "
            f"{rec['calls_succeeded']} | "
            f"${spent:.4f} | {ovr_str} | "
            f"{abort_at if abort_at is not None else '—'} | "
            f"`{exc}` |"
        )
    lines.append(
        "\n¹ Wire calls = HTTP calls observed by the mock LLM "
        "(the source of ground-truth $). Runner-reported succeeded is "
        "what the runner *thinks* completed before its library aborted. "
        "These can differ if a library lets a call complete on the wire "
        "before raising (post-call enforcement)."
    )

    lines.append("")
    lines.append("## Per-runner detail\n")
    for rec in runners:
        runner = rec["runner"]
        truth = ground.get(
            runner,
            {"calls": 0, "cost_usd": 0.0, "input_tokens": 0,
             "output_tokens": 0, "model": "?"},
        )
        budget = float(rec["budget_usd"])
        spent = truth["cost_usd"]
        lines.append(f"### `{runner}`\n")
        lines.append(f"- Budget configured: **${budget:.2f}**")
        lines.append(f"- Model under test: `{truth['model']}`")
        lines.append(
            f"- Calls reaching the wire (mock LLM): **{truth['calls']}**"
        )
        lines.append(
            f"- Total input tokens: {truth['input_tokens']:,} · "
            f"output tokens: {truth['output_tokens']:,}"
        )
        lines.append(
            f"- Ground-truth $ spent: **${spent:.4f}** "
            f"(overshoot **{overshoot_pct(spent, budget):+.1f}%**)"
        )
        lines.append(f"- Calls attempted by runner: {rec['calls_attempted']}")
        lines.append(f"- Calls succeeded (per runner): {rec['calls_succeeded']}")
        if rec.get("abort_at_call") is not None:
            lines.append(
                f"- Aborted at call **#{rec['abort_at_call']}** with "
                f"`{rec.get('abort_exception_class')}`"
            )
            reason = rec.get("abort_reason")
            if reason:
                lines.append(f"  - Reason: `{reason[:200]}`")
        else:
            lines.append("- Did **not** abort within MAX_CALLS")
        lines.append(
            f"- Self-reported spent: `{rec.get('self_reported_spent')}`"
        )
        lines.append(
            f"- Self-reported remaining: `{rec.get('self_reported_remaining')}`"
        )
        lines.append(f"- Wall time: {rec.get('elapsed_seconds')}s\n")

    lines.append("## Methodology\n")
    lines.append(
        "- Each runner is configured with the same budget and a hard "
        "ceiling of 100 attempted calls.\n"
        "- All calls hit a shared mock OpenAI-compatible endpoint that "
        "returns deterministic usage tokens.\n"
        "- The mock LLM logs every call (model, tokens, runner) to a "
        "shared volume; the analyzer multiplies `(tokens × published "
        "price)` to get the ground-truth dollar number.\n"
        "- Each runner self-reports its enforcement view; we surface "
        "both numbers so any drift between what the library *thinks* "
        "it stopped at and what *actually* hit the wire is visible.\n"
        "- Pricing source: published OpenAI / Anthropic list prices, "
        "snapshot embedded in `analyze/analyze.py`.\n"
    )
    lines.append("## Reproducing\n")
    lines.append("```bash\nmake benchmark\n```\n")
    lines.append("See [`README.md`](./README.md) for full setup.")
    return "\n".join(lines) + "\n"


def main() -> None:
    runners = load_runner_records()
    ground = aggregate_mock_log()

    output = render_markdown(runners, ground)
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_PATH.write_text(output)
    if HOST_COPY.parent.exists():
        try:
            shutil.copy(OUTPUT_PATH, HOST_COPY)
        except (OSError, PermissionError) as exc:
            print(f"[analyze] could not copy to host bind: {exc}")
    print(output)


if __name__ == "__main__":
    main()
